                <span class="first-word">Chafchaouen</span> also known as
                Chaouen, is a city in northwest Morocco. It is the chief town of
                the province of the same name, and is noted for its buildings in
                shades of blue. Chefchaouen is situated just inland from Tangier
                and Tétouan.
              </p>
              <p>
                Chefchaouen – or Chaouen, as it is often called by Moroccans –
                is a popular tourist destination because of its proximity to
                Tangier and the Spanish enclave of Ceuta. There are
                approximately two hundred hotels catering to the summer influx
                of European tourists. One distinction possessed by Chefchaouen
                is its blue-rinsed houses and buildings.
              </p>
              <p>
                Chefchaouen is a popular shopping destination as well, as it
                offers many native handicrafts that are not available elsewhere
                in Morocco, such as wool garments and woven blankets. The goat
                cheese native to the area is also popular with tourists.
              </p>

              <details>
                <summary>History of Chefchaouen</summary>
                <p>
                  The city was founded in 1471 as a small kasbah (fortress) by
                  Moulay Ali ibn Rashid al-Alami, a descendant of Abd as-Salam
                  al-Alami and Idris I, and through them, of the Islamic prophet
                  Muhammad. Al-Alami founded the city to fight the Portuguese
                  invasions of northern Morocco. Along with the Ghomara tribes
                  of the region, many Moriscos and Jews settled here after the
                  Spanish Reconquista in medieval times.[3] In 1920, the Spanish
                  seized Chaouen to form part of Spanish Morocco. Spanish troops
                  imprisoned Abd el-Krim el-Khattabi in the kasbah from 1916 to
                  1917, after he talked with the German consul Dr. Walter
                  Zechlin (1879–1962).
                </p>
                <p>
                  In September 1925, in the middle of the Rif War, a rogue
                  squadron of American volunteer pilots, including veterans of
                  World War I, bombarded civilians in Chaouen. Colonel Charles
                  Michael Sweeney had proposed the idea to French Prime Minister
                  Paul Painlevé, who "warmly welcomed the Colonel’s request."
                </p>
                <p>
                  After al-Khattabi was defeated with the help of the French, he
                  was deported to Réunion in 1926. Spain returned the city after
                  the independence of Morocco in 1956.
                </p>
              </details>

              <p>
                Chefchaouen's blue walls are a popular subject of interest.
                There are several theories as to why the walls were painted
                blue. One popular theory is that the blue keeps mosquitos away,
                another is that Jews introduced the blue when they took refuge
                from Hitler in the 1930s. The blue is said to symbolize the sky
                and heaven, and serve as a reminder to lead a spiritual life.
                However, according to some locals, the walls were mandated to be
                painted blue simply to attract tourists at some point in the
                1970s.
              </p>

