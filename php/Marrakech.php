              <p>
                <span class="first-word">Marrakech</span> is the fourth largest
                city in the Kingdom of Morocco. It is the capital of the
                mid-southwestern region of Marrakesh-Safi. It is located west of
                the foothills of the Atlas Mountains. Marrakesh is situated 580
                km (360 mi) southwest of Tangier, 327 km (203 mi) southwest of
                the Moroccan capital of Rabat, 239 km (149 mi) south of
                Casablanca, and 246 km (153 mi) northeast of Agadir.
              </p>
              <p>
                The region has been inhabited by Berber farmers since Neolithic
                times. The city was founded in 1062, by Abu Bakr ibn Umar, a
                chieftain and cousin of the Almoravid king, Yusuf ibn Tashfin,
                as the imperial capital of the Almoravid Empire. The city was
                one of Morocco's four imperial cities. In the 12th century, the
                Almoravids built many madrasas (Quranic schools) and mosques in
                Marrakesh that bear Andalusian influences. The red walls of the
                city, built by Ali ibn Yusuf in 1122–1123, and various buildings
                constructed in red sandstone during this period, have given the
                city the nickname of the "Red City" or "Ochre City". Marrakesh
                grew rapidly and established itself as a cultural, religious,
                and trading center for the Maghreb and sub-Saharan Africa. Jemaa
                el-Fnaa is the busiest square in Africa.
              </p>

              <details>
                <summary>Learn more about history of Marrakech</summary>
                <p>
                  After a period of decline, the city was surpassed by Fez, but
                  in the early 16th century, Marrakesh again became the capital
                  of the kingdom. The city regained its preeminence under
                  wealthy Saadian sultans Abu Abdallah al-Qaim and Ahmad
                  al-Mansur, who embellished the city with sumptuous palaces
                  such as the El Badi Palace (1578) and restored many ruined
                  monuments. Beginning in the 17th century, the city became
                  popular among Sufi pilgrims for Morocco's seven patron saints,
                  who are entombed here. In 1912 the French Protectorate in
                  Morocco was established and T'hami El Glaoui became Pasha of
                  Marrakesh and held this position nearly throughout the
                  protectorate until the role was dissolved upon the
                  independence of Morocco and the reestablishment of the
                  monarchy in 1956. In 2009, Marrakesh mayor Fatima Zahra
                  Mansouri became the second woman to be elected mayor in
                  Morocco.
                </p>
                <p>
                  Marrakesh comprises an old fortified city packed with vendors
                  and their stalls. This medina quarter is a UNESCO World
                  Heritage Site. Today it is one of the busiest cities in Africa
                  and serves as a major economic center and tourist destination.
                  Tourism is strongly advocated by the reigning Moroccan
                  monarch, Mohammed VI, with the goal of doubling the number of
                  tourists visiting Morocco to 20 million by 2020. Despite the
                  economic recession, real estate and hotel development in
                  Marrakesh have grown dramatically in the 21st century.
                  Marrakesh is particularly popular with the French, and
                  numerous French celebrities own property in the city.
                  Marrakesh has the largest traditional market (souk) in
                  Morocco, with some 18 souks selling wares ranging from
                  traditional Berber carpets to modern consumer electronics.
                  Crafts employ a significant percentage of the population, who
                  primarily sell their products to tourists.
                </p>
              </details>

              <p>
                Marrakesh is served by Ménara International Airport and by
                Marrakesh railway station which connects the city to Casablanca
                and northern Morocco. Marrakesh has several universities and
                schools, including Cadi Ayyad University. A number of Moroccan
                football clubs are located here, including Najm de Marrakech,
                KAC Marrakech, Mouloudia de Marrakech and Chez Ali Club de
                Marrakech. The Marrakesh Street Circuit hosts the World Touring
                Car Championship, Auto GP and FIA Formula Two Championship
                races.
              </p>

